// var declarations with a single uninitialized variable
var foo;
// var declarations with a single initialized variable
var foo2 = 10;
// var declarations with multiple variables
var foo3 = 20, foo4;

// let declarations with a single uninitialized variable
let bar;
// let declarations with a single initialized variable
let bar2 = 10;
// let declarations with multiple variables
let bar3 = 20, bar4;

// const declarations with a single variable
const baz = 10;
// const declarations with multiple variables
const baz2 = 20, baz3 = 30;
