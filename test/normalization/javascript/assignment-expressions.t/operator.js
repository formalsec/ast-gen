// assignment expression with an unary operator
foo = -10;
// assignment expression with a binary operator
foo = 10 + "abc";
// assignment expression with a logical operator
foo = 10 && "abc";
// assignment expression with a conditional operator
foo = true ? 10 : "abc";
// assignment expression with a prefix update operator
foo = ++bar;
// assignment expression with a postfix update operator
foo = bar++;
// assignment expression with a prefix member update operator
foo = --bar[baz];
// assignment expression with a postfix member update operator
foo = bar[baz]++;
