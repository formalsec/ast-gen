// function expression with an alias and no parameters 
(function () { });
// function expression with an alias and a single parameter
(function (foo) { });
// async function expression with no parameters
(async function () { });
// async function expression with a single parameter
(async function (foo) { });
// generator function expression with no parameters
(function* () { });
// generator function expression with a single parameter
(function* (foo) { });
// async generator function expression with no parameters
(async function* () { });
// async generator function expression with a single parameter
(async function* (foo) { });
